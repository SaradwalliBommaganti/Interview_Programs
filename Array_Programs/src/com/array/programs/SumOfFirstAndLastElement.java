package com.array.programs;

public class SumOfFirstAndLastElement {

      public static void main(String args[]){
        int[] arr = {1, 3, 5, 6, 10};
        int i=arr[0];
        int j=arr[arr.length-1];
        int sum=i+j;
        System.out.println("Adding two numbers first and last:"+sum);
    }


}
